int Ipv6_getcount(S_Element* pElement)
{
    ASSERT_NULL(pElement);

    return sizeof(Ipv6_table) / 16;
}

char* Ipv6_getvalue(S_Element* pElement, int pos)
{

    int i = 0;

    ASSERT_NULL(pElement);

    set_ElementInitoutBuf_ex(pElement, 16);

    hw_Memcpy(pElement->para.value, pElement->inBuf, 16);

    //şó64bit
    if (pos == 0) {
        for (i = 64; i <= 127; i++) {
            FILL_BIT(pElement->para.value, i);
        }
    }

    if (pos == 1) {
        for (i = 64; i <= 127; i++) {
            ZERO_BIT(pElement->para.value, i);
        }
    }

    if (pos == 2) {
        for (i = 64; i <= 126; i++) {
            ZERO_BIT(pElement->para.value, i);
        }

        FILL_BIT(pElement->para.value, 127);
    }

    // mac
    if (pos == 3) {
        pElement->para.value[11] = 0xff;
        pElement->para.value[12] = 0xfe;
    }

    // subnet id
    if (pos == 4) {
        for (i = 48; i <= 63; i++) {
            FILL_BIT(pElement->para.value, i);
        }
    }

    if (pos == 5) {
        for (i = 48; i <= 63; i++) {
            ZERO_BIT(pElement->para.value, i);
        }
    }

    if (pos == 6) {
        for (i = 48; i <= 62; i++) {
            ZERO_BIT(pElement->para.value, i);
        }

        FILL_BIT(pElement->para.value, 63);
    }

    // global id
    if (pos == 7) {
        for (i = 16; i <= 47; i++) {
            FILL_BIT(pElement->para.value, i);
        }
    }

    if (pos == 8) {
        for (i = 16; i <= 47; i++) {
            ZERO_BIT(pElement->para.value, i);
        }
    }

    if (pos == 9) {
        for (i = 16; i <= 46; i++) {
            ZERO_BIT(pElement->para.value, i);
        }

        FILL_BIT(pElement->para.value, 47);
    }

    // 2000
    if (pos == 10) {
        for (i = 0; i <= 47; i++) {
            ZERO_BIT(pElement->para.value, i);
        }

        pElement->para.value[00] = 0x20;
        pElement->para.value[01] = 0x00;
    }

    // ff00
    if (pos == 10) {
        for (i = 0; i <= 47; i++) {
            ZERO_BIT(pElement->para.value, i);
        }

        pElement->para.value[00] = 0xff;
        pElement->para.value[01] = 0x00;
    }

    // ff01
    if (pos == 11) {
        for (i = 0; i <= 47; i++) {
            ZERO_BIT(pElement->para.value, i);
        }

        pElement->para.value[00] = 0xff;
        pElement->para.value[01] = 0x01;
    }

    // ff80
    if (pos == 12) {
        for (i = 0; i <= 47; i++) {
            ZERO_BIT(pElement->para.value, i);
        }

        pElement->para.value[00] = 0xff;
        pElement->para.value[01] = 0x80;
    }

    // ffc0
    if (pos == 13) {
        for (i = 0; i <= 47; i++) {
            ZERO_BIT(pElement->para.value, i);
        }

        pElement->para.value[00] = 0xff;
        pElement->para.value[01] = 0xc0;
    }

    // fc00
    if (pos == 14) {
        for (i = 0; i <= 47; i++) {
            ZERO_BIT(pElement->para.value, i);
        }

        pElement->para.value[00] = 0xfc;
        pElement->para.value[01] = 0x00;
    }

    // fd00
    if (pos == 15) {
        for (i = 0; i <= 47; i++) {
            ZERO_BIT(pElement->para.value, i);
        }

        pElement->para.value[00] = 0xfd;
        pElement->para.value[01] = 0x00;
    }

    if (pos >= 16) {
        // ip¶ŕ˛Ą
        pos = pos - 16;
        for (i = 0; i < 16; i++)
            pElement->para.value[i] = Ipv6_table[pos][i];
    }

    return pElement->para.value;
}

int Ipv6_getissupport(S_Element* pElement)
{
    ASSERT_NULL(pElement);
    if (pElement->para.type == enum_Ipv6)
        return enum_Yes;

    return enum_No;
}

const struct Mutater_group Ipv6_group = {"Ipv6", Ipv6_getcount, Ipv6_getvalue, Ipv6_getissupport, 1};

void init_Ipv6(void)
{
    register_Mutater(&Ipv6_group, enum_MIpv6);
}
